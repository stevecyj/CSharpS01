int num = Convert.ToInt32(Console.ReadLine());
switch (num)
{
    case 1:
        Console.WriteLine("礦泉水");
        break;
    case 2:
        Console.WriteLine("紅茶");
        break;
    case 3:
        Console.WriteLine("綠茶");
        break;
    case 4:
        Console.WriteLine("雪碧");
        break;
    case 5:
        Console.WriteLine("可口可樂");
        break;
    default:
        Console.WriteLine("脈動");
        break;
}